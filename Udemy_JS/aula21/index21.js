/* Operadores de Comparação

> - maior que
>= - maior ou igual 
< - menor que
<= - menor ou igual
== - igualdade (valor) ***** não usar esse
=== - igualdade estrita (valor e tipo) sempre utilizar esse
!= - diferente (valor) ***** não usa esse
!== - diferente estrito (valor e tipo) sempre utilizar esse

*/
